/*********************************************************************
*
*       Dialog callback routine
*/

static void _cbCallback3(WM_MESSAGE * pMsg) {
  int i;
  int NCode, Id;
  WM_HWIN hDlg, hItem,hCheck;
  hDlg = pMsg->hWin;
  switch (pMsg->MsgId) 
  {
    case WM_PAINT:
     
    return;
    break;
    
    case WM_INIT_DIALOG:
    
        /* Init EDIT-widgets */
      if(speed_front_on){CHECKBOX_Check(WM_GetDialogItem(hDlg, GUI_ID_CHECK1));}//select
	  else		  {CHECKBOX_Uncheck(WM_GetDialogItem(hDlg, GUI_ID_CHECK1));}
	  
      if(dahua_on){CHECKBOX_Check(WM_GetDialogItem(hDlg, GUI_ID_CHECK2));}//select
	  else		  {CHECKBOX_Uncheck(WM_GetDialogItem(hDlg, GUI_ID_CHECK2));}

      if(baosi_on){CHECKBOX_Check(WM_GetDialogItem(hDlg, GUI_ID_CHECK3));}//select
	  else		  {CHECKBOX_Uncheck(WM_GetDialogItem(hDlg, GUI_ID_CHECK3));}

      if(kanke_on){CHECKBOX_Check(WM_GetDialogItem(hDlg, GUI_ID_CHECK4));}//select
	  else		  {CHECKBOX_Uncheck(WM_GetDialogItem(hDlg, GUI_ID_CHECK4));}

      if(nining_on){CHECKBOX_Check(WM_GetDialogItem(hDlg, GUI_ID_CHECK5));}//select
	  else		  {CHECKBOX_Uncheck(WM_GetDialogItem(hDlg, GUI_ID_CHECK5));
     break;


    case WM_NOTIFY_PARENT:
      Id    = WM_GetId(pMsg->hWinSrc);      /* Id of widget */
      NCode = pMsg->Data.v;                 /* Notification code */
      switch (NCode) 
      {
        case WM_NOTIFICATION_RELEASED:      /* React only if released */
          if (Id == GUI_ID_OK)
          {            /* OK Button */
      						 
             GUI_EndDialog(hDlg, 0);
             TouchXY.TouchX = TouchXY.TouchY = 0;
		     i_menu = 0;      
          }
          
          
          if (Id == GUI_ID_CANCEL)
          {        /* Cancel Button */
            GUI_EndDialog(hDlg, 1);
            TouchXY.TouchX = TouchXY.TouchY = 0;
		    i_menu = 0;      
          }
          
           if(Id == GUI_ID_CHECK1)
			{
  			hCheck = WM_GetDialogItem(hDlg, GUI_ID_CHECK1);
  			speed_front_on= CHECKBOX_IsChecked(hCheck);
 			 }

           if(Id == GUI_ID_CHECK2)
			{
  			hCheck = WM_GetDialogItem(hDlg, GUI_ID_CHECK2);
  			dahua_on= CHECKBOX_IsChecked(hCheck);

 			 }

          if(Id == GUI_ID_CHECK3)
			{
  			hCheck = WM_GetDialogItem(hDlg, GUI_ID_CHECK3);
  			baosi_on= CHECKBOX_IsChecked(hCheck);

 			 }

          if(Id == GUI_ID_CHECK4)
			{
  			hCheck = WM_GetDialogItem(hDlg, GUI_ID_CHECK4);
  			kanke_on= CHECKBOX_IsChecked(hCheck);

 			 }

          if(Id == GUI_ID_CHECK5)
			{
  			hCheck = WM_GetDialogItem(hDlg, GUI_ID_CHECK5);
  			nining_on= CHECKBOX_IsChecked(hCheck);

 			 }


          break;
          }
        case WM_NOTIFICATION_VALUE_CHANGED: /* Value has changed */
          _OnValueChanged3(hDlg, Id);
          
          break;
      }
      break;
    default:
      WM_DefaultProc(pMsg);
  }
}




