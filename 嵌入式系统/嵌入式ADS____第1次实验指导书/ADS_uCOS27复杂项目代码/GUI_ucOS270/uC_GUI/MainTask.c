extern volatile int  count_pingtan ;
extern volatile int  count_nining ;
extern volatile int  count_kanke ;
extern volatile float  steady ;

 void rizhi() {
 
  int i;
  float per;
  PROGBAR_Handle ahProgBar[4];

  GUI_SetColor(GUI_WHITE);
  GUI_SetFont(&GUI_FontHZ16);
  GUI_DispStringAt("平坦路", 20,25);
  GUI_DispStringAt("泥泞路", 20,55);
  GUI_DispStringAt("坎坷路", 20,85);
  
  GUI_SetColor(GUI_CYAN);
  GUI_DispStringAt("最高速度:", 20,150);
  GUI_DispStringAt("平均速度:", 20,200);
  GUI_DispStringAt("汽车平稳度:", 20,250);

  GUI_DispStringAt("km/h" , 180, 150 );
  GUI_DispStringAt("km/h" , 180, 200 );
   GUI_DispStringAt("%" , 180, 250 );
  
  for (i=0; i<=2; i++)
  {
  /* Create the progbars */  
  ahProgBar[i] = PROGBAR_Create(80 ,25 + 30*i,135,20, WM_CF_SHOW);
  
  PROGBAR_SetBarColor(ahProgBar[i], 0, GUI_BLUE);
  PROGBAR_SetBarColor(ahProgBar[i], 1, GUI_GRAY);
  PROGBAR_SetMinMax(ahProgBar[i], 0, 100);

  /* Use memory device (optional, for better looks) */
  PROGBAR_EnableMemdev(ahProgBar[i]);
  }
  
  ahProgBar[3] = PROGBAR_Create(20 ,290,195,10, WM_CF_SHOW);
  PROGBAR_SetBarColor(ahProgBar[3], 0, GUI_GREEN);
  PROGBAR_SetBarColor(ahProgBar[3], 1, GUI_GRAY);
  PROGBAR_EnableMemdev(ahProgBar[3]);
  
  
  while(0 == TouchXY.TouchX)
  {
  
  per = ((float)count_pingtan / (float)(count_pingtan + count_nining + count_kanke)) * 100;
  PROGBAR_SetValue(ahProgBar[0], per);
  steady = per ;
  
  per = ((float)count_nining / (float)(count_pingtan + count_nining + count_kanke)) * 100;
  PROGBAR_SetValue(ahProgBar[1], per);
  steady = steady + per * 0.3;
  
  per = ((float)count_kanke / (float)(count_pingtan + count_nining + count_kanke)) * 100;
  PROGBAR_SetValue(ahProgBar[2], per);
  steady = steady + per * 0.1;
  
  if(steady > 50)PROGBAR_SetBarColor(ahProgBar[3], 0, GUI_GREEN);
  else PROGBAR_SetBarColor(ahProgBar[3], 0, GUI_RED);
  PROGBAR_SetValue(ahProgBar[3], steady);
  GUI_Exec();
  
  
  }

  PROGBAR_Delete(ahProgBar[0]);
  PROGBAR_Delete(ahProgBar[1]);
  PROGBAR_Delete(ahProgBar[2]);
  PROGBAR_Delete(ahProgBar[3]);
   
}


