/*******************************************************************
*
*                     GetAngle
*
********************************************************************

This routine returns the value value to indicate. In a real application,
this value would somehow be measured.
*/

static float GetAngle(float tDiff) {
  if (tDiff < 15000) {
    return  225 - 0.006 * tDiff ;
  }
  tDiff -= 15000;
  if (tDiff < 7500) {
    return  225 - 90 + 0.012 * tDiff ;
  }
  tDiff -= 7000;
  return 225;
}

